# Print 5 to 1

i = 5
while i >= 1:
    print(i)
    i -= 1

print("==============")

#Countdown Timer

i = 10
while i >= 1:
    print(i)
    i -= 1
print("Time's up!")

print("==============")

#Reverse Even Numbers

i = 10
while i >= 2:
    print(i)
    i -= 2

print("==============")

#Product of Numbers

i = 5
prod = 1
while i >= 1:
    prod *= i
    i -= 1
print("Product:", prod)

print("==============")

#Exit Condition Loop

num = 1

while num != 0:
    num = int(input("Enter number (0 to stop): "))