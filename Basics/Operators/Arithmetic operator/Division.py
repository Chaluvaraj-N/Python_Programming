# 1. floor div = // (return non-decimal quotient)

print(10 // 2)

print(20 // 4)

print(100 // 10)

print(22 // 7)


# 2. float div = / (return decimal quotient)

print(10 / 2)

print(20 / 4)

print(100 / 10)

print(22 / 7)


# 3. modulas = % (return remainder)

print(100 % 3)

print(10 % 5)

print(22 % 7)


#Note

print(999 % 2999) # if numerator < demametor then it will print numerator only

print(10 % 1000)

